% Matrix operations are fundamental in MATLAB. They include addition, subtraction, multiplication, and division.  
% Md. Roknuzzaman Rikon
% 26-09-24  

A = [1 2; 3 4];  
B = [5 6; 7 8];  

% Matrix Addition  
C = A + B;  

% Matrix Subtraction  
D = A - B;  

% Matrix Multiplication  
E = A * B;  

% Element-wise Multiplication  
F = A .* B;  

% Matrix Division  
G = A / B;  

% Display Results  
disp('Matrix Addition:'), disp(C)  
disp('Matrix Subtraction:'), disp(D)  
disp('Matrix Multiplication:'), disp(E)  
disp('Element-wise Multiplication:'), disp(F)  
disp('Matrix Division:'), disp(G)  
